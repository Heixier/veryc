#include "libft.h"

char	**free_2darr(int k, char **arr)
{
	if (k == 0 || arr == NULL)
		return (NULL);
	k--;
	while (k >= 0)
	{
		free(arr[k]);
		k--;
	}
	free(arr);
	return (NULL);
}
